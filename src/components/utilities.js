
// Hack to avoid needing to use CSS Modules
export default function modifyVariables(colorString, mediumColorString, mediumStrokeString) {
  const root = document.documentElement;
  root.style.setProperty('--main-theme-color', colorString);
  root.style.setProperty('--medium-text-fill-color', mediumColorString);
  root.style.setProperty('--medium-text-stroke-color', mediumStrokeString);
}
